name = "Harry"

print(name[0:3])

print(name[-4: -1])     # it is just like print(name[1:4]) 

print(name[:4])   # means [0:4]
print(name[1:])   # means [1:last word], in this case print(name[1:4])
print(name[1:7])  # irrespective of the number mentioned, it ends at the last word



a = "amazing"
print(a[1:6:2])  # last colon skips mentioned digits & only prints the written numbered letter.



