friends = ["Apple", "Orange", 5, 345.06, False, "Akash", "Rohan"]

print(friends[5])

# we can change an item of list. But we can't change an item of a string. 
friends[0] = "Grapes"
print(friends[0])   # unlike strings lists are mutable.

# list slicing:
print(friends[1:6])  # - will give results from 1 to 6

